/**
 * Model 3 distribution matrix test (Story 7.3).
 *
 * Verifies:
 *   AC-1: compiler-present install compiles from source → byte-identical to golden
 *   AC-2: compiler-absent install uses precompiled fallback → byte-identical to AC-1
 *   AC-3: divergence between compiled and precompiled SKILL.md is detected and reported
 *
 * Per Story 7.3 spec resolutions:
 *   OQ-1=A: minimal Model 3 fallback ships with this story (in invoke-python.js + installer.js).
 *   OQ-2=A: mock not needed — AC-2 calls `applyModel3FallbackIfAllEligible` directly (DN-4=G3).
 *   OQ-3=A: golden SKILL.md pre-committed at the fixture path (generated by compile.py once).
 *   OQ-4=A: in-test mutation for AC-3 divergent content (no second fixture dir).
 *   DN-1=Y: AC-3 compiles at test-run time so the assertion is non-tautological.
 *   DN-3=F1: fixture has `model3-test-module/` intermediate dir (compile.py uses parent.name).
 *   DN-4=G3: routing helper `applyModel3FallbackIfAllEligible` exported and called directly.
 *
 * Usage: node test/test-model3-distribution-matrix.js
 */

'use strict';

const crypto = require('node:crypto');
const fs = require('node:fs/promises');
const os = require('node:os');
const path = require('node:path');

const { runBatchInstall, applyModel3FallbackIfAllEligible } = require('../tools/installer/compiler/invoke-python');

const colors = { reset: '[0m', green: '[32m', red: '[31m', dim: '[2m' };
let passed = 0;
let failed = 0;

function assert(condition, testName, errorMessage = '') {
  if (condition) {
    console.log(`${colors.green}✓${colors.reset} ${testName}`);
    passed++;
  } else {
    console.log(`${colors.red}✗${colors.reset} ${testName}`);
    if (errorMessage) console.log(`  ${colors.dim}${errorMessage}${colors.reset}`);
    failed++;
  }
}

async function runTest(name, fn) {
  try {
    await fn();
  } catch (error) {
    console.log(`${colors.red}✗${colors.reset} ${name}`);
    console.log(`  ${colors.dim}threw: ${error.message}${colors.reset}`);
    failed++;
  }
}

async function hashFile(filePath) {
  const content = await fs.readFile(filePath);
  return crypto.createHash('sha256').update(content).digest('hex');
}

/**
 * Shallow directory copy via readdir + copyFile (no `fs.cp` — that API is marked
 * experimental until Node 22.3.0 per eslint-plugin-n; project supports Node >=20).
 * Sufficient for the Model 3 fixture, which has only top-level files (no subdirectories).
 *
 * ECH-6 / AA-2 (Phil R1 2026-05-08): runtime guard rejects subdirectories explicitly so a
 * future fixture growth (e.g. adding a `fragments/` subdir) fails loudly here rather than
 * producing a confusing wrong-hash compile error downstream.
 */
async function copyDirShallow(srcDir, destDir) {
  await fs.mkdir(destDir, { recursive: true });
  const entries = await fs.readdir(srcDir, { withFileTypes: true });
  const subdirs = entries.filter((e) => e.isDirectory()).map((e) => e.name);
  if (subdirs.length > 0) {
    throw new Error(`copyDirShallow: fixture has subdirectories (${subdirs.join(', ')}) — update to recursive copy or flatten the fixture`);
  }
  for (const entry of entries) {
    if (!entry.isFile()) continue;
    await fs.copyFile(path.join(srcDir, entry.name), path.join(destDir, entry.name));
  }
}

// R2-AA-1 (Phil R2 2026-05-08): FIXTURE_* const block hoisted above
// `assertFixtureGoldenSelfConsistent` for lexical clarity. Function references
// `FIXTURE_PRECOMPILED` and `FIXTURE_GOLDEN_HASH` at module scope; declaring them
// first removes the legal-but-surprising "use before declaration" pattern (TDZ-safe
// today because the function only runs after main() awaits, but fragile if the
// helper is ever extracted to a separate module).
const FIXTURE_SOURCE = path.join(__dirname, 'fixtures', 'model3-test-module', 'source');
// DN-3=F1: module-name intermediate dir required — compile.py uses parent.name as installDir subpath.
const FIXTURE_MODULE_DIR = path.join(FIXTURE_SOURCE, 'model3-test-module');
const FIXTURE_SKILL_DIR = path.join(FIXTURE_MODULE_DIR, 'test-skill');
const FIXTURE_PRECOMPILED = path.join(FIXTURE_SKILL_DIR, 'SKILL.md');
// Regenerate FIXTURE_GOLDEN_HASH:
//   sha256sum test/fixtures/model3-test-module/source/model3-test-module/test-skill/SKILL.md
const FIXTURE_GOLDEN_HASH = '49d635fd32f9bd031436ea132478b97bbf7ca1c2664b76d03841368997ac5667';

// bmadDir for runBatchInstall — compile.py is at <bmadDir>/scripts/compile.py.
// invoke-python.js:162 calls fs.access(compilePy) immediately, so bmadDir must be a real
// directory containing scripts/compile.py. The repo's `src/` directory satisfies this.
const BMAD_DIR_FOR_COMPILE = path.join(__dirname, '..', 'src');

/**
 * BH-6 / ECH-5 (Phil R1 2026-05-08): assert the on-disk golden SKILL.md hash matches the
 * test's hardcoded `FIXTURE_GOLDEN_HASH` constant before running ACs. Self-diagnoses a stale
 * constant: a developer who regenerates the golden but forgets to update the constant gets
 * a clear "update FIXTURE_GOLDEN_HASH to: <actual>" message instead of a confusing AC-1
 * hash-mismatch failure that's hard to triage.
 *
 * R2-ECH-2 (Phil R2 2026-05-08): missing-fixture branch surfaces a "regenerate via compile.py"
 * hint instead of letting the raw ENOENT propagate from hashFile.
 */
async function assertFixtureGoldenSelfConsistent() {
  let actual;
  try {
    actual = await hashFile(FIXTURE_PRECOMPILED);
  } catch (error) {
    if (error && error.code === 'ENOENT') {
      throw new Error(
        `FIXTURE_PRECOMPILED missing at ${FIXTURE_PRECOMPILED}. ` +
          `Restore from git, or regenerate via:\n` +
          `  python3 src/scripts/compile.py --skill ${FIXTURE_SKILL_DIR} --install-dir <tmp>\n` +
          `Then copy <tmp>/test-skill/SKILL.md back to ${FIXTURE_PRECOMPILED}.`,
      );
    }
    throw error;
  }
  if (actual !== FIXTURE_GOLDEN_HASH) {
    throw new Error(
      `FIXTURE_GOLDEN_HASH stale: on-disk SKILL.md hashes to ${actual}, ` +
        `but the test constant is ${FIXTURE_GOLDEN_HASH}. ` +
        `Update FIXTURE_GOLDEN_HASH in test/test-model3-distribution-matrix.js to: ${actual}`,
    );
  }
}

async function main() {
  // R2-BH-5 (Phil R2 2026-05-08): MUST BE FIRST — fixture-golden self-consistency must run
  // before any AC test. Do NOT add any work above this call. A stale FIXTURE_GOLDEN_HASH
  // constant gets a clear "update to <actual>" message; a missing fixture gets a clear
  // regenerate-via-compile.py hint. Anything that bypasses this check turns AC-1/AC-2 hash
  // failures into confusing triage exercises.
  try {
    await assertFixtureGoldenSelfConsistent();
  } catch (error) {
    console.log(`${colors.red}✗${colors.reset} FIXTURE self-consistency`);
    console.log(`  ${colors.dim}${error.message}${colors.reset}`);
    failed++;
    console.log(`\n${passed + failed} tests: ${passed} passed, ${failed} failed`);
    process.exit(1);
  }

  await runTest('AC-1: compiler-present install compiles from source', async () => {
    const tmpInstall = await fs.mkdtemp(path.join(os.tmpdir(), 'bmad-model3-ac1-'));
    try {
      // compile.py --batch enforces an OVERRIDE_OUTSIDE_ROOT security check that requires
      // the skill_dir to be inside install_dir (or under a shared scenario_root). Copy the
      // fixture into the install tree before compiling — mirrors the Story 7.2 setUp pattern
      // (full skill source tree at <install_dir>/<module>/<skill>/). compile.py overwrites
      // the precompiled SKILL.md with the freshly compiled output.
      const installedSkillDir = path.join(tmpInstall, 'model3-test-module', 'test-skill');
      await copyDirShallow(FIXTURE_SKILL_DIR, installedSkillDir);

      await runBatchInstall({
        skills: [{ skillDir: installedSkillDir, installDir: tmpInstall }],
        bmadDir: BMAD_DIR_FOR_COMPILE,
        projectRoot: process.cwd(),
      });
      const installedPath = path.join(tmpInstall, 'model3-test-module', 'test-skill', 'SKILL.md');
      const exists = await fs
        .access(installedPath)
        .then(() => true)
        .catch(() => false);
      assert(exists, 'AC-1: SKILL.md written to install dir', `expected: ${installedPath}`);
      if (!exists) return;
      const hash = await hashFile(installedPath);
      assert(hash === FIXTURE_GOLDEN_HASH, 'AC-1: compiled output byte-identical to golden', `got ${hash}, want ${FIXTURE_GOLDEN_HASH}`);
    } finally {
      // R2-BH-6 (Phil R2 2026-05-08): swallow EBUSY/EPERM on Windows where python3 may
      // hold file handles momentarily after exit (antivirus / search indexer race). The
      // tempdir leak is preferable to a misleading test-cleanup failure that masks the
      // actual AC verdict.
      await fs.rm(tmpInstall, { recursive: true, force: true }).catch(() => {});
    }
  });

  await runTest('AC-2: compiler-absent install uses precompiled fallback', async () => {
    const tmpInstall = await fs.mkdtemp(path.join(os.tmpdir(), 'bmad-model3-ac2-'));
    try {
      // DN-4=G3: call routing helper directly. Exercises:
      //   detectModelTier(FIXTURE_SKILL_DIR) → 'model3' → copyPrecompiledFallback().
      // No Python subprocess invoked (the helper only does fs.readdir + fs.copyFile).
      const skills = [{ skillDir: FIXTURE_SKILL_DIR, installDir: tmpInstall }];
      const applied = await applyModel3FallbackIfAllEligible(skills);
      assert(
        applied === true,
        'AC-2: routing helper applied fallback (all skills are Model 3)',
        `applyModel3FallbackIfAllEligible returned ${applied}`,
      );

      const installedPath = path.join(tmpInstall, 'model3-test-module', 'test-skill', 'SKILL.md');
      const exists = await fs
        .access(installedPath)
        .then(() => true)
        .catch(() => false);
      assert(exists, 'AC-2: SKILL.md written (fallback path)', `expected: ${installedPath}`);
      if (!exists) return;
      const hash = await hashFile(installedPath);
      assert(
        hash === FIXTURE_GOLDEN_HASH,
        'AC-2: fallback output byte-identical to golden (= AC-1 output)',
        `got ${hash}, want ${FIXTURE_GOLDEN_HASH}`,
      );
    } finally {
      // R2-BH-6 (Phil R2 2026-05-08): swallow EBUSY/EPERM on Windows where python3 may
      // hold file handles momentarily after exit (antivirus / search indexer race). The
      // tempdir leak is preferable to a misleading test-cleanup failure that masks the
      // actual AC verdict.
      await fs.rm(tmpInstall, { recursive: true, force: true }).catch(() => {});
    }
  });

  await runTest('AC-3: divergence between compiled and precompiled fails with hash report', async () => {
    // DN-1=Y: compile from source at test-run time so the assertion is non-tautological —
    // a hash-comparison bug or buffer-handling regression causes this test to fail rather
    // than pass silently against a pre-stored constant.
    const tmpInstall = await fs.mkdtemp(path.join(os.tmpdir(), 'bmad-model3-ac3-'));
    let compiledHash;
    let setupError;
    try {
      // Same OVERRIDE_OUTSIDE_ROOT layout requirement as AC-1: copy fixture into install tree.
      const installedSkillDir = path.join(tmpInstall, 'model3-test-module', 'test-skill');
      await copyDirShallow(FIXTURE_SKILL_DIR, installedSkillDir);
      await runBatchInstall({
        skills: [{ skillDir: installedSkillDir, installDir: tmpInstall }],
        bmadDir: BMAD_DIR_FOR_COMPILE,
        projectRoot: process.cwd(),
      });
      const installedPath = path.join(tmpInstall, 'model3-test-module', 'test-skill', 'SKILL.md');
      compiledHash = await hashFile(installedPath);
    } catch (error) {
      // BH-4 (Phil R1 2026-05-08): label setup failures as "AC-3 setup failed" rather than
      // letting the runTest catch surface them as a generic "threw: <msg>". Captures the
      // error here, lets finally run, then explicitly asserts with the labeled context.
      setupError = error;
    } finally {
      // R2-BH-6 (Phil R2 2026-05-08): swallow EBUSY/EPERM on Windows where python3 may
      // hold file handles momentarily after exit (antivirus / search indexer race). The
      // tempdir leak is preferable to a misleading test-cleanup failure that masks the
      // actual AC verdict.
      await fs.rm(tmpInstall, { recursive: true, force: true }).catch(() => {});
    }
    if (setupError) {
      assert(false, 'AC-3 setup failed (compile step did not produce a hash)', setupError.message);
      return;
    }
    // Guard: if compile succeeded but hashFile returned falsy somehow — defensive.
    if (!compiledHash) {
      assert(false, 'AC-3 setup failed (live compile produced no hash)');
      return;
    }

    // OQ-4=A: in-test mutation simulates a divergent (stale or hand-edited) precompiled
    // SKILL.md. No second fixture directory needed.
    //
    // R2-ECH-4 (Phil R2 2026-05-08) — CRITICAL: do NOT `fs.writeFile` to FIXTURE_PRECOMPILED.
    // The mutation MUST stay in-memory only. The fixture is the AC-1/AC-2 byte-identity
    // golden; corrupting it cascades into every subsequent run (assertFixtureGoldenSelfConsistent
    // would fail next time, but the corruption is committable and would block CI). If a future
    // refactor seems to "simplify" this by writing the mutated content to disk, STOP — the
    // current pattern is correct.
    const precompiledContent = await fs.readFile(FIXTURE_PRECOMPILED, 'utf8');
    const divergentContent = precompiledContent + '\n<!-- DIVERGED: intentional test mutation -->\n';
    const divergentHash = crypto.createHash('sha256').update(Buffer.from(divergentContent)).digest('hex');

    // CI gate: compiledHash (live) MUST differ from divergentHash (mutated). If hash
    // comparison has a bug (always-equal), this assert fails and the gate is enforced.
    assert(
      compiledHash !== divergentHash,
      `AC-3: hash mismatch report: compiled=${compiledHash} precompiled=${divergentHash} skill=test-skill/SKILL.md`,
      'Hashes must differ for divergence detection to be valid',
    );

    // R2-ECH-4 post-test re-check: if the test ABOVE accidentally wrote to FIXTURE_PRECOMPILED
    // (regression from a future refactor), the on-disk SKILL.md hash now differs from
    // FIXTURE_GOLDEN_HASH and this re-check fails loudly on the SAME run that introduced the
    // corruption — instead of poisoning every subsequent test run.
    try {
      await assertFixtureGoldenSelfConsistent();
    } catch (error) {
      assert(false, 'AC-3 corrupted FIXTURE_PRECOMPILED on disk (R2-ECH-4 regression guard)', error.message);
    }
  });

  console.log(`\n${passed + failed} tests: ${passed} passed, ${failed} failed`);
  process.exit(failed > 0 ? 1 : 0);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
